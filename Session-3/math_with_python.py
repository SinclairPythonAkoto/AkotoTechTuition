# from math import floor, ceil, sqrt

# basic math operators
print(f"10 multiply by 2: {10 * 2}")
print(f"10 divide by 2: {10 / 2}")
print(f"10 add 2: {10 + 2}")
print(f"10 subtract 2: {10 - 2}")

# exponent / raise to the power of
print(f"4 to the power of 3: {4 ** 3}")

# modulo
print(f"10 modulo 3: {10 % 3}")

# order of operations
print(3 * (4 + 5))

# math built-in function: abs()
print(abs(-5))

# math built-n function: pow()
print(pow(4, 3))

# math built-in function: max()
print(max(10, 199, 50))

# math built-in function: min()
print(min(8, 90, 31, 5, 948, 198, 16, 258, 10, 809, 3))

# math built-in function: round()
print(round(1.5))


