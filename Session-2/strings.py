name = "Sinclair"

print(name)

# .lower()
print(name.lower())

# .upper()
print(name.upper())

# isupper()
print(name.isupper())

# islower()
print(name.islower())

# len()
print(len(name))

# string index
print(name[0:3])

# .index()
print(name.index("c"))

# .replace
print(name.replace("Sinclair", "John"))
